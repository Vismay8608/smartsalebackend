
-- CLIENT REGISTRATION ONBOARDING STATUSES

CREATE TABLE client_register (
    clientid        INTEGER     GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    client_category VARCHAR(10) CHECK (client_category IN ('SYSTEM','SELLER','BUYER')),
    client_type     VARCHAR(15) CHECK (client_type IN ('COMPANY','INDIVIDUAL')),
    mobile_primary             VARCHAR(20),
    mobile_primary_verified    BOOLEAN     NOT NULL DEFAULT FALSE,
    mobile_primary_verified_at TIMESTAMPTZ,
    email_primary              VARCHAR(255),
    email_primary_otp          VARCHAR(10),
    email_primary_verified_at  TIMESTAMPTZ,
    address_line1 TEXT,
    address_line2 TEXT,
    city          VARCHAR(100),
    state         VARCHAR(100),
    country       VARCHAR(100) NOT NULL DEFAULT 'India',
    pincode       VARCHAR(10),
    registration_status VARCHAR(40) NOT NULL DEFAULT 'REGISTRATION_STARTED' CHECK (registration_status IN ('REGISTRATION_STARTED','KYC_PENDING','ONBOARDING_COMPLETE')),
    accountstatus VARCHAR(30) NOT NULL DEFAULT 'ACTIVE' CHECK (accountstatus IN ('ACTIVE','INACTIVE','LOCKED')),
    adminusername VARCHAR(100),
    adminuserid   INTEGER,
    created_by INTEGER,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ,
    updated_by INTEGER
);

CREATE INDEX idx_cr_category_type ON client_register(client_category, client_type);
CREATE INDEX idx_cr_status         ON client_register(registration_status, accountstatus);


-- ============================================================
-- USERS â€” created early so downstream tables can reference it.
-- branchid FK to client_branches is deferred via ALTER TABLE
-- after client_branches is created (avoids circular dependency).
-- ============================================================
CREATE TABLE user_register (
    userid INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    clientid INTEGER NOT NULL REFERENCES client_register(clientid),
    branchid INTEGER,   -- FK added after client_branches via ALTER TABLE below
    username VARCHAR(100) NOT NULL UNIQUE,
    email VARCHAR(255) NOT NULL UNIQUE,
    phone_number VARCHAR(20),
    phone_verified BOOLEAN DEFAULT FALSE,
    phone_verified_at TIMESTAMPTZ,
    password_hash VARCHAR(500) NOT NULL,
    password_salt VARCHAR(100) NOT NULL,
    user_type VARCHAR(25) NOT NULL CHECK (user_type IN ('SYSTEM','SELLER','BUYER')),
    account_status VARCHAR(30) DEFAULT 'ACTIVE' CHECK (account_status IN ('ACTIVE','INACTIVE','LOCKED')),
    is_active BOOLEAN DEFAULT TRUE,
    email_verified BOOLEAN DEFAULT FALSE,
    email_verified_at TIMESTAMPTZ,
    mfa_enabled BOOLEAN DEFAULT FALSE,
    mfa_type VARCHAR(10) CHECK (mfa_type IN ('TOTP','SMS_OTP','EMAIL','NONE')),
    mfa_secret VARCHAR(255),
    force_password_change BOOLEAN DEFAULT TRUE,
    last_login_at TIMESTAMPTZ,
    previous_login_at TIMESTAMPTZ,
    password_changed_at TIMESTAMPTZ,
    password_expiry_date TIMESTAMPTZ,
    version INTEGER NOT NULL DEFAULT 1,
    deleted_at TIMESTAMPTZ,
    created_by INTEGER REFERENCES user_register(userid),
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ,
    updated_by INTEGER
);

CREATE INDEX IF NOT EXISTS idx_users_username       ON user_register(username);
CREATE INDEX IF NOT EXISTS idx_users_email          ON user_register(email);
CREATE INDEX IF NOT EXISTS idx_users_client_id      ON user_register(clientid);
CREATE INDEX IF NOT EXISTS idx_users_account_status ON user_register(account_status);


