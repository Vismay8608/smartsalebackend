CREATE TABLE client_documents (
    doc_id            INTEGER      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    clientid          INTEGER      REFERENCES client_register(clientid) ON DELETE CASCADE,
    user_id           INTEGER      REFERENCES user_register(userid) ON DELETE SET NULL,
    doc_type          VARCHAR(30)  NOT NULL CHECK (doc_type IN ('AADHAAR','PAN_INDIVIDUAL','SELFIE','ADDRESS_PROOF','GST_CERTIFICATE','CIN_CERTIFICATE','PAN_COMPANY','ADDRESS_PROOF_COMP','INCORPORATION_CERT','AUTH_SIGNATORY','BANK_PROOF','OTHER')),
    doc_number_masked VARCHAR(30),
    doc_hash          VARCHAR(64),
    storage_uri       TEXT         NOT NULL,
    file_name         VARCHAR(255),
    file_size_kb      INTEGER,
    mime_type         VARCHAR(100),
    doc_status        VARCHAR(10)  NOT NULL DEFAULT 'PENDING' CHECK (doc_status IN ('EXPIRED','PENDING','VERIFIED')),
    expiry_date       DATE,
    remarks           TEXT,
    uploaded_by       INTEGER      REFERENCES user_register(userid),
    uploaded_at       TIMESTAMPTZ  NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_cdoc_client ON client_documents(clientid, doc_type);
CREATE INDEX idx_cdoc_user   ON client_documents(user_id,  doc_type);
CREATE INDEX idx_cdoc_status ON client_documents(doc_status, uploaded_at DESC);
CREATE INDEX idx_cdoc_hash   ON client_documents(doc_hash)  WHERE doc_hash IS NOT NULL;


CREATE TABLE client_user_kyc (
    kyc_id            INTEGER      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    clientid          INTEGER      NOT NULL REFERENCES client_register(clientid) ON DELETE CASCADE,
    user_id           INTEGER      REFERENCES user_register(userid) ON DELETE SET NULL,
    -- FIX: valid kyc_subject values (original had 'SELLER_EMPLOYEE' but check used wrong literals)
    kyc_subject       VARCHAR(20)  NOT NULL CHECK (kyc_subject IN ('SYSTEM_EMPLOYEE','SELLER_COMPANY','SELLER_EMPLOYEE','SELLER_INDIVIDUAL','BUYER_COMPANY','BUYER_INDIVIDUAL')),
    kyc_status        VARCHAR(10)  NOT NULL DEFAULT 'PENDING' CHECK (kyc_status IN ('EXPIRED','PENDING','VERIFIED')),
    gst_masked        VARCHAR(15),
    gst_hash          VARCHAR(64),
    cin_masked        VARCHAR(21),
    cin_hash          VARCHAR(64),
    full_name         VARCHAR(200),
    date_of_birth     DATE,
    pan_masked        VARCHAR(10),
    pan_hash          VARCHAR(64),
    aadhaar_masked    VARCHAR(12),
